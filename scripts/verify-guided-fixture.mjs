import { spawnSync } from "node:child_process";
import { readFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const target = resolve(root, "test-fixtures/vulnerable-app");
const prioritiesPath = resolve(target, ".reporook/priorities.json");
const priorities = JSON.parse(await readFile(prioritiesPath, "utf8"));
const first = priorities.priorities?.[0];
if (!first?.finding_id || first.priority !== "fix-now") throw new Error("The fixture did not produce a fix-now priority");
if (priorities.tool?.version !== "0.3.0") throw new Error("Priorities were not generated by RepoRook 0.3.0");
if (priorities.coverage_status !== "complete") throw new Error("Guided fixture priorities must come from complete scanner coverage");

const result = spawnSync(process.execPath, [resolve(root, "cli/dist/index.js"), "plan", first.finding_id, target, "--quiet"], { encoding: "utf8" });
if (result.status !== 0) throw new Error(`Could not prepare fixture remediation plan: ${result.stderr}`);
const directory = resolve(target, `.reporook/remediations/${first.finding_id}`);
const plan = JSON.parse(await readFile(resolve(directory, "plan.json"), "utf8"));
const prompt = await readFile(resolve(directory, "fix-prompt.txt"), "utf8");
if (plan.tool?.version !== "0.3.0" || plan.finding?.id !== first.finding_id || plan.approval?.status !== "pending") {
  throw new Error("The fixture remediation plan is not bound to the selected finding with pending approval");
}
if (!plan.approval.binds_to?.includes("exact-patch") || !plan.approval.binds_to?.includes("test-plan")) {
  throw new Error("The fixture remediation plan does not bind approval to the patch and tests");
}
if (!/exact diff/i.test(prompt) || !/Do not treat approval/i.test(prompt)) {
  throw new Error("The fixture fix prompt does not enforce exact-proposal approval");
}
process.stdout.write(`Verified guided fix queue and approval-bound plan for ${first.finding_id}.\n`);
